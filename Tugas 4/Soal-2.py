# SOAL 2: Operasi Matematika

# 1. Buat dua variabel
a = 20
b = 1

# 2. Hitung hasil operasi matematika
penjumlahan = a + b
pengurangan = a - b
perkalian = a * b
pembagian = a / b     # Pembagian float
modulus = a % b       # Sisa bagi
pangkat = a ** b      # Pangkat (a dipangkatkan b)

# 3. Tampilkan hasilnya dalam format yang jelas
print("\n--- Soal 2: Operasi Matematika ---")
print(f"{a} + {b} = {penjumlahan}")
print(f"{a} - {b} = {pengurangan}")
print(f"{a} * {b} = {perkalian}")
print(f"{a} / {b} = {pembagian}")
print(f"{a} % {b} = {modulus}")
print(f"{a} ** {b} = {pangkat}")
print("------------------------------------")